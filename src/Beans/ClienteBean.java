package Beans;

public class ClienteBean {
	
	private int id_cliente;
	private String nome_cliente;
	private String cnpj_cliente;
	private String senha_cliente;
	private String confirmarSenha_cliente;
	private String localizacao_cliente;
	private String cep_cliente;
	private String telefone_cliente;
	private String email_cliente;
	
	
	public int getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(int id_cliente) {
		this.id_cliente = id_cliente;
	}
	public String getNome_cliente() {
		return nome_cliente;
	}
	public void setNome_cliente(String nome_cliente) {
		this.nome_cliente = nome_cliente;
	}
	public String getCnpj_cliente() {
		return cnpj_cliente;
	}
	public void setCnpj_cliente(String cnpj_cliente) {
		this.cnpj_cliente = cnpj_cliente;
	}
	public String getSenha_cliente() {
		return senha_cliente;
	}
	public void setSenha_cliente(String senha_cliente) {
		this.senha_cliente = senha_cliente;
	}
	public String getConfirmarSenha_cliente() {
		return confirmarSenha_cliente;
	}
	public void setConfirmarSenha_cliente(String confirmarSenha_cliente) {
		this.confirmarSenha_cliente = confirmarSenha_cliente;
	}
	public String getLocalizacao_cliente() {
		return localizacao_cliente;
	}
	public void setLocalizacao_cliente(String localizacao_cliente) {
		this.localizacao_cliente = localizacao_cliente;
	}
	public String getCep_cliente() {
		return cep_cliente;
	}
	public void setCep_cliente(String cep_cliente) {
		this.cep_cliente = cep_cliente;
	}
	public String getTelefone_cliente() {
		return telefone_cliente;
	}
	public void setTelefone_cliente(String telefone_cliente) {
		this.telefone_cliente = telefone_cliente;
	}
	public String getEmail_cliente() {
		return email_cliente;
	}
	public void setEmail_cliente(String email_cliente) {
		this.email_cliente = email_cliente;
	}
	
	
	
}
